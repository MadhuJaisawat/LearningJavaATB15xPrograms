package ex_07_IncreDecreeOperator;

public class IncrementOperator {
    public static void main(String[] args) {
        int a = 9;
        int result = ++a;
        System.out.println(result);
        System.out.println(a);
        int DecResult = --a;
        System.out.println(DecResult);
        System.out.println(a);
    }
}
