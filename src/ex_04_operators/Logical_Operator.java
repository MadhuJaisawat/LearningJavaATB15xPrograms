package ex_04_operators;

public class Logical_Operator {
    public static void main(String[] args) {
        boolean a = true;
        System.out.println(a);
        System.out.println(!a);
        System.out.println(!!a);
        boolean c = true || false;
        System.out.println(c);
        boolean b = true && false;
        System.out.println(b);
    }
}
