package ex_10_ForLoop;

public class forloop_interview {
    public static void main(String[] args) {
        for (int i = 0; i>1; i++) {
            System.out.println(i);
        }
        // like it will check for first condition i=0 and if i>1 soit will be false .
        System.out.println("End");
    }
}
