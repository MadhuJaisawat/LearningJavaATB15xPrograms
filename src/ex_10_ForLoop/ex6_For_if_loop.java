package ex_10_ForLoop;

public class ex6_For_if_loop {
    public static void main(String[] args) {
        for (int i =0 ;i <10 ; i++) {
            if (i >6 ) {
                System.out.println("No is greater than 6");
            } else {
                System.out.println("no is less than 6");
            }
        }
    }
}
