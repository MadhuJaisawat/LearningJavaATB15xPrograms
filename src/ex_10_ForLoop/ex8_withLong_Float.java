package ex_10_ForLoop;

public class ex8_withLong_Float {
    public static void main(String[] args) {
        for (long i = 1l ; i<20 ; i++) {
            System.out.println(i);
        }
        for (float f = 0.0f;f<10.3; f++) {
            System.out.println(f);
            //not very useful
        }
    }
}
