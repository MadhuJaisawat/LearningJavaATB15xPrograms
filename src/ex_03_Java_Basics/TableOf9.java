package ex_03_Java_Basics;

public class TableOf9 {
    public static void main(String[] args) {
        int tab = 9;
        System.out.println("Following is Table of 9");
        System.out.printf("%d*1=%d%n", tab, tab*1);
        System.out.printf("%d*2=%d%n", tab, tab*2);
        System.out.printf("%d*3=%d%n", tab, tab*3);
        System.out.printf("%d*4=%d%n", tab, tab*4);
        System.out.printf("%d*5=%d%n", tab, tab*5);
        System.out.printf("%d*6=%d%n", tab, tab*6);
        System.out.printf("%d*7=%d%n", tab, tab*7);
        System.out.printf("%d*8=%d%n", tab, tab*8);
        System.out.printf("%d*9=%d%n", tab, tab*9);
        System.out.printf("%d*10=%d%n", tab, tab*10);
    }
}
